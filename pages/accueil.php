<h1>Mon Avis (en quelques mots)</h1>
<p>Selon une idée qui depuis longtemps m'intéresse, j'ai conçu ce site qui provient du concept dit de « Web CV ». Il s'agit d'un Curriculum Vitæ interactif, exploitant les possibilités offertes par les technologies liées à l'Internet, afin d'aller au delà de la - plus commune - dimension « papier » du CV.</p>
